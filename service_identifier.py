class ServiceIdentifier:
    def __init__(self, banner_timeout: float = 2.0):
        self.banner_timeout = banner_timeout

    def identify_by_port(self, port: int) -> str:
        return COMMON_PORTS.get(port, "unknown")

    def grab_banner(self, host: str, port: int, timeout: float = None) -> Optional[str]:
        if timeout is None:
            timeout = self.banner_timeout

        try:
            with socket.socket(socket.AF_INET, socket.SOCK_STREAM) as sock:
                sock.settimeout(timeout)
                sock.connect((host, port))

                try:
                    sock.send(b"HEAD / HTTP/1.1\r\nHost: " + host.encode() + b"\r\n\r\n")
                except:
                    pass

                try:
                    data = sock.recv(4096)
                    if data:
                        return data.decode("utf-8", errors="replace").strip()
                except:
                    pass

                return None
        except:
            return None

    def identify_by_banner(self, banner: str) -> str:
        if not banner:
            return None

        banner_bytes = banner.encode("utf-8", errors="ignore")
        for signature, service in BANNER_SERVICES.items():
            if signature in banner_bytes:
                return service

        if "HTTP" in banner or "http" in banner:
            return "HTTP"
        if "SSH" in banner or "ssh" in banner:
            return "SSH"
        if "FTP" in banner or "ftp" in banner:
            return "FTP"
        if "SMTP" in banner or "smtp" in banner:
            return "SMTP"

        return None

    def identify_service(self, host: str, port: int, grab_banner: bool = True) -> tuple[str, Optional[str]]:
        banner = None

        if grab_banner:
            banner = self.grab_banner(host, port)

        if banner:
            banner_service = self.identify_by_banner(banner)
            if banner_service:
                return banner_service, banner

        port_service = self.identify_by_port(port)
        return port_service, banner
